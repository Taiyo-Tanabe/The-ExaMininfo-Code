def _ensure(db: Session, key: str) -> models.SiteContent:
    obj = db.query(models.SiteContent).filter(models.SiteContent.key == key).first()
    if not obj:
        obj = models.SiteContent(key=key, value=DEFAULT_CONTENTS.get(key, ""))
        db.add(obj)
        db.commit()
        db.refresh(obj)
    return obj


def get_content(db: Session, key: str) -> models.SiteContent:
    return _ensure(db, key)


def update_content(db: Session, key: str, data: schemas.SiteContentUpdate) -> models.SiteContent:
    obj = _ensure(db, key)
    obj.value = data.value
    db.commit()
    db.refresh(obj)
    return obj
